public class TwoNumbers {
  public static void main(String[] args) {
    double a = 13;
    double b = 22;
    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    double c = a / b;
    System.out.println(c);
    System.out.println(a / b);
    System.out.println(b % a);
  }
}
